(function () {
  theme.Modals = (function () {
    function Modal(id, name, options) {
      const defaults = {
        close: '.js-modal-close',
        open: `.js-modal-open-${name}`,
        openClass: 'modal--is-active',
        closingClass: 'modal--is-closing',
        bodyOpenClass: ['modal-open'],
        bodyClosingClass: 'modal-closing',
        closeOffContentClick: true,
      };

      this.id = id;
      this.modal = document.getElementById(id);

      if (!this.modal) {
        console.warn(`Modal with ID "${id}" not found.`);
        return;
      }

      this.modalContent = this.modal.querySelector('.modal__inner');
      this.config = Object.assign(defaults, options);
      this.modalIsOpen = false;

      this.init();
    }

    Modal.prototype.init = function () {
      // Bind open buttons
      document.querySelectorAll(this.config.open).forEach((btn) => {
        btn.setAttribute('aria-expanded', 'false');
        btn.addEventListener('click', this.open.bind(this));
      });

      // Bind close buttons
      this.modal.querySelectorAll(this.config.close).forEach((btn) => {
        btn.addEventListener('click', this.close.bind(this));
      });

      // Close modal on outside click
      this.modal.addEventListener('click', (evt) => {
        if (this.config.closeOffContentClick && !evt.target.closest('.modal__inner')) {
          this.close(evt);
        }
      });

      // Close modal if a drawer is opened
      document.addEventListener('drawerOpen', this.close.bind(this));
    };

    Modal.prototype.open = function (evt) {
      if (this.modalIsOpen) return;

      if (evt) {
        evt.preventDefault();
        evt.stopPropagation();
        this.activeSource = evt.currentTarget;
        this.activeSource.setAttribute('aria-expanded', 'true');
      }

      this.modal.classList.add(this.config.openClass);
      document.documentElement.classList.add(...this.config.bodyOpenClass);

      this.modalIsOpen = true;

      document.dispatchEvent(new CustomEvent('modalOpen', { detail: { id: this.id } }));
    };

    Modal.prototype.close = function (evt) {
      if (!this.modalIsOpen) return;

      if (evt && evt.target.closest('.js-modal-close')) {
        // Close button clicked
      } else if (evt && evt.target.closest('.modal__inner')) {
        // Click inside modal content, do nothing
        return;
      }

      this.modal.classList.remove(this.config.openClass);
      this.modal.classList.add(this.config.closingClass);

      document.documentElement.classList.remove(...this.config.bodyOpenClass);
      document.documentElement.classList.add(this.config.bodyClosingClass);

      setTimeout(() => {
        this.modal.classList.remove(this.config.closingClass);
        document.documentElement.classList.remove(this.config.bodyClosingClass);

        if (this.activeSource) {
          this.activeSource.setAttribute('aria-expanded', 'false');
          this.activeSource.focus();
        }

        this.modalIsOpen = false;
        document.dispatchEvent(new CustomEvent('modalClose', { detail: { id: this.id } }));
      }, 500); // Match CSS transition duration
    };

    return Modal;
  })();
})();

// ------------------ Cookie 操作工具 ------------------//
function setCookie(name, value, days) {
  const date = new Date();
  date.setTime(date.getTime() + days * 24 * 60 * 60 * 1000);
  document.cookie = `${name}=${value}; expires=${date.toUTCString()}; path=/`;
}

function getCookie(name) {
  const cookies = document.cookie.split('; ');
  for (const cookie of cookies) {
    const [key, value] = cookie.split('=');
    if (key === name) return value;
  }
  return null;
}

function deleteCookie(name) {
  document.cookie = `${name}=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/`;
}

// ------------------ 初始化订阅弹窗 ------------------//
document.addEventListener('DOMContentLoaded', function () {
  const sectionElement = document.querySelector('[data-section-id]');
  if (!sectionElement) {
    return;
  }

  const sectionId = sectionElement.getAttribute('data-section-id');
  const cookieName = `newsletter-popup-${sectionId}`;

  // 检查 Cookie 是否存在
  if (getCookie(cookieName) === 'true') {
    return;
  }

  // 初始化弹窗
  const newsletterModal = new theme.Modals(`NewsletterPopup-${sectionId}`, 'newsletter');

  // 打开弹窗并设置 Cookie
  newsletterModal.open();
  setCookie(cookieName, 'true', 7);

  // 示例：通过按钮触发弹窗
  document.querySelector('.newsletter-trigger').addEventListener('click', function (e) {
    e.preventDefault();
    newsletterModal.open();
    deleteCookie(`newsletter-popup-${sectionId}`);
  });
});
